package controle;

public class For1 {
	
	public static void main(String[] args) {
		
		
		for(int contador = 10; contador > 1; contador -= 2) {
			
			System.out.printf("i = %d\n", contador);
		}
		int contador = 0;
		
		while(contador <= 10) {
			System.out.printf("i = %d\n", contador);
			contador += 2;
			
		}
		
		
	}

}
